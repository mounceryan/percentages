# Imports the NumPy Python library.
import numpy

# Defines 'arr' as a NumPy array from 1 to 5 incluive.
arr = numpy.array([1, 2, 3, 4, 5])

# Prints 'arr'.
print(arr)



# Imports the NumPy Python library using the alias 'np'. 'np' is literally just an easier way to refer to NumPy. The alias can be anything.
import numpy as np

# Defines 'arr' as a NumPy array from 1 to 5 incluive.
arr = np.array([1, 2, 3, 4, 5])

# Prints 'arr'.
print(arr)



# Imports the NumPy Python library using the alias 'np'.
import numpy as np

# Prints the NumPy version.
print(np.__version__)